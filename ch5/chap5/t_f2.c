#include<stdio.h>
#include<stdlib.h>

int main()
{
	if (1)
		printf("1 is TRUE.\n");
	else
		printf("1 is FALSE.\n");

	if (0)
		printf("0 is TRUE.\n");
	else 
		printf("0 is FALSE.\n");

	if (14)
		printf("14 is TRUE.\n");
	else
		printf("14 is FALSE.\n");

	if (-62)
		printf("-62 is TRUE.\n");
	else
		printf("-62 is FALSE.\n");
		
	system("PAUSE");
	return 0;
}
